   private void afficherTableTransactions(List listeTransactions) {
       
        Vector donneesTable = new Vector();
        Vector<String> entete = new Vector<String>();
        entete.add("Id transactions");
        entete.add("Code emplacement");
        entete.add("Heure debut");
        entete.add("Heure fin");
        entete.add("Date");
        entete.add("Username");
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat tf = new SimpleDateFormat("hh:mm");

        for (Object object : listeTransactions) {
            Transaction trx = (Transaction) object;
            Vector ligne = new Vector();
            ligne.add(trx.getIdTransaction());
            ligne.add(trx.getCodeEmplacement());
            ligne.add(tf.format(trx.getHeureDebut().getTime()));
            ligne.add(tf.format(trx.getHeureFin().getTime()));
            ligne.add(df.format(trx.getDate().getTime()));
            ligne.add(trx.getUser().getUserName());

            donneesTable.add(ligne);
        }

        tblTransactions.setModel(new DefaultTableModel(donneesTable, entete));

    } 

    private void afficherTableUtilisateurs(List listeUtilisateurs) {
       
        Vector donneesTable = new Vector();
        Vector<String> entete = new Vector<String>();
        entete.add("Username");
        entete.add("Nom");
        entete.add("Prénom");
      

        for (Object object : listeUtilisateurs) {
            User usr = (User) object;
            Vector ligne = new Vector();
            ligne.add(usr.getUserName());
            ligne.add(usr.getNom());
            ligne.add(usr.getPrenom());
            donneesTable.add(ligne);
        }

        tblUtilisateurs.setModel(new DefaultTableModel(donneesTable, entete));
    }

    private void afficherUtilisateur(User user) {
        txtUsername.setText(user.getUserName());
        txtPassword.setText(user.getPassword());
        txtPrenom.setText(user.getPrenom());
        txtNom.setText(user.getNom());
        txtNumTel.setText(user.getNumTelephone());
        txtSolde.setText(new Double(user.getSolde()).toString());
        
    }

