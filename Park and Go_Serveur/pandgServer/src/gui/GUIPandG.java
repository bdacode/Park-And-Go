    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        User user = new User();
        String userName = txtUsername.getText();
        user.setUserName(userName);
        user.setPassword(txtPassword.getText());
        user.setNom(txtNom.getText());
        user.setPrenom(txtPrenom.getText());
        user.setNumTelephone(txtNumTel.getText());
        try{
            user.setSolde(Double.parseDouble(txtSolde.getText()));
           int err = Services.enregistrerUtilisateur(user);
           switch(err){
               case Services.SUCCESS :resetUsersScr(); JOptionPane.showMessageDialog(null,txtUsername.getText()+" enregistré avec succès", "Information", JOptionPane.INFORMATION_MESSAGE); 
                        afficherListeUtilisateurs();break;
               case Services.CONNECTION_ERROR: JOptionPane.showMessageDialog(null,"L'enregistrement a échoué, \n problème de connexion","Problème de connexion" , err); break;
               case Services.DB_ERROR : JOptionPane.showMessageDialog(null, "Ce nom d'utilisateur n'est pas disponible","Erreur de saisie" , JOptionPane.ERROR_MESSAGE); break;
               default : break;    
           }
          
        }catch(NumberFormatException e){
          JOptionPane.showMessageDialog(null, "Le solde doit être entré en chiffres","Erreur de saisie" , JOptionPane.ERROR_MESSAGE);
        }
       
           
       
        
    }//GEN-LAST:event_btnAjouterActionPerformed

